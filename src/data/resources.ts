const text = (name: string, label: string, options: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'text', ...options })
const textarea = (name: string, label: string, options: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'textarea', ...options })
const date = (name: string, label: string, options: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'date', ...options })
const number = (name: string, label: string, options: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'number', ...options })
const bool = (name: string, label: string, options: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'boolean', ...options })
const select = (name: string, label: string, options: readonly string[], extra: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'select', options, ...extra })
const multi = (name: string, label: string, options: readonly string[], extra: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'multi', options, ...extra })
const relation = (name: string, label: string, resource: string, extra: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'relation', resource, ...extra })
const relationMulti = (name: string, label: string, resource: string, extra: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'relation-multi', resource, ...extra })
const fundRepeater = (name: string, label: string, options: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'fund-repeater', ...options })
const inventorRepeater = (name: string, label: string, options: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'inventor-repeater', ...options })
const applicantRepeater = (name: string, label: string, options: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'applicant-repeater', ...options })
const licensorRepeater = (name: string, label: string, options: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'licensor-repeater', ...options })
const milestoneRepeater = (name: string, label: string, options: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'milestone-repeater', ...options })
const ipRelation = (name: string, label: string, dependsOn = 'ip_type', extra: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'ip-relation', dependsOn, ...extra })
const ipMulti = (name: string, label: string, dependsOn = 'ip_type', extra: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'ip-multi', dependsOn, ...extra })
const json = (name: string, label: string, options: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'json', ...options })
const file = (name: string, label: string, options: Partial<FieldConfig> = {}): FieldConfig => ({ name, label, type: 'file', ...options })

